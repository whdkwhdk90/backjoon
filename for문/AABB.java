package backjoon;

import java.util.Scanner;

public class AABB {
	public static void main(String[] args) {
		byte a,b,T;
		
	Scanner sc = new Scanner(System.in);
	T = sc.nextByte();
	
	for(int i =1; i <= T; i++)
	{
		a= sc.nextByte();
		b= sc.nextByte();
		System.out.println(a+b);
		
	}
		
		
	}
		
	}
